// Toggle curriculum level expansion
function toggleCurriculumLevel(curriculumId, level) {
    const levelContent = document.getElementById(`${curriculumId}-${level}`);
    if (levelContent) {
        levelContent.style.display = levelContent.style.display === 'none' ? 'block' : 'none';
    }
}

